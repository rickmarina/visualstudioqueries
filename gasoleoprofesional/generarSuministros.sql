SELECT TCF65.ID_MOV AS IDMOVCONT, TCF65.FECHA_MOV AS FECHA, TCF65.HORA_MOV AS HORA, TCF66.CL_ARTI AS CODPRO, 
		CONVERT(decimal(7, 2), CASE WHEN TCF65.TIPO_MOV = 'AC' THEN TCF66.UNIDADES * -1 ELSE TCF66.UNIDADES END, 2) AS LIT,  
		TCF14CI.CL_TARJETA as cl_tarjeta, TCF14CI.C_VENTA as c_venta 
		FROM   TCF65 INNER JOIN 
		TCF14CI ON TCF65.CL_EMPRESA = TCF14CI.CL_EMPRESA AND TCF65.CL_CENTRO = TCF14CI.C_VENTA AND 
		TCF65.CL_TPV = TCF14CI.P_VENTA AND TCF65.N_VENTA = TCF14CI.N_VENTA AND 
		TCF65.FECHA_MOV = TCF14CI.FECHA INNER JOIN 
		TCF66 ON TCF65.ID_MOV = TCF66.ID_MOV 
		WHERE  (TCF14CI.C_RESULTADO = '000') AND 
		(TCF14CI.T_OPERACION = 'C' OR TCF14CI.T_OPERACION = 'A') AND 
		(TCF66.CL_ARTI = '130' OR TCF66.CL_ARTI = '133')  
AND (DATEDIFF(day, CONVERT(datetime, dbo.TCF65.FECHA_MOV, 112), GETDATE()) = 5) 

